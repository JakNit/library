CREATE DATABASE IF NOT EXISTS library;

CREATE TABLE `library`.`authors` ( `id` INT(11) NOT NULL AUTO_INCREMENT , `name` VARCHAR(100) NOT NULL , PRIMARY KEY (`id`)) ENGINE = InnoDB;

CREATE TABLE `library`.`books` ( `id` INT(11) NOT NULL AUTO_INCREMENT , `name` VARCHAR(100) NOT NULL , `ISBN` VARCHAR(100) NOT NULL , `price` FLOAT NOT NULL , `category` VARCHAR(100) NOT NULL , `author` INT(11) NOT NULL , PRIMARY KEY (`id`)) ENGINE = InnoDB;